        private void ELE(Object N) {
            if (te > ts || text) {
                int raw = -1;
                int rawlen = 0;
                ele_open = false;
                text = false;

                if (ts != -1 && N != sym_cdata && N != sym_text && N != sym_procins && N != sym_comment) {
                    raw = ts;
                    rawlen = te - ts;
                }

                if (_blockParam != null) {
                    MutableString raw_string = null;
                    if (raw > 0) {
                        raw_string = MutableString.Create(new String(buf, raw, rawlen));
                    }
                    // NOTE: right before v0.7 the fourth argument of rb_yield_tokens was yielding raw_string 
                    //       but now it yields null (hardcoded). I still have to understand why, but this 
                    //       might be a way to limit the memory usage of Hpricot given that raw_string was 
                    //       not really that used in userland.
                    rb_yield_tokens(N, tag[0], attr, null, taint);
                }
                else {
                    rb_hpricot_token(_state, N, tag[0], attr, raw, rawlen, taint);
                }
            }
        }

        private void SET(Object[] N, int E) {
            if (N == tag) {
                if (mark_tag == -1 || E == mark_tag) {
                    tag[0] = MutableString.Empty;
                }
                else if (E > mark_tag) {
                    tag[0] = MutableString.Create(new String(buf, mark_tag, E - mark_tag));
                }
            }
            else if (N == akey) {
                if (mark_akey == -1 || E == mark_akey) {
                    akey[0] = MutableString.Empty;
                }
                else if (E > mark_akey) {
                    akey[0] = MutableString.Create(new String(buf, mark_akey, E - mark_akey));
                }
            }
            else if (N == aval) {
                if (mark_aval == -1 || E == mark_aval) {
                    aval[0] = MutableString.Empty;
                }
                else if (E > mark_aval) {
                    aval[0] = MutableString.Create(new String(buf, mark_aval, E - mark_aval));
                }
            }
        }

        private void CAT(Object[] N, int E) {
            if (N[0] == null) {
                SET(N, E);
            }
            else {
                int mark = 0;
                if (N == tag) {
                    mark = mark_tag;
                }
                else if (N == akey) {
                    mark = mark_akey;
                }
                else if (N == aval) {
                    mark = mark_aval;
                }
                (N[0] as MutableString).Append(new String(buf, mark, E - mark));
            }
        }

        private void SLIDE(Object N) {
            int mark = 0;
            if (N == tag) {
                mark = mark_tag;
            }
            else if (N == akey) {
                mark = mark_akey;
            }
            else if (N == aval) {
                mark = mark_aval;
            }
            if (mark > ts) {
                if (N == tag) {
                    mark_tag -= ts;
                }
                else if (N == akey) {
                    mark_akey -= ts;
                }
                else if (N == aval) {
                    mark_aval -= ts;
                }
            }
        }

        private void ATTR(Object K, Object V) {
            if (K != null) {
                if (attr == null) {
                    attr = new Hash(_currentContext);
                }
                (attr as Hash)[K] = V;
            }
        }

        private void ATTR(Object[] K, Object V) {
            ATTR(K[0], V);
        }

        private void ATTR(Object K, Object[] V) {
            ATTR(K, V[0]);
        }

        private void ATTR(Object[] K, Object[] V) {
            ATTR(K[0], V[0]);
        }

        private void TEXT_PASS() {
            if (!text) {
                if (ele_open) {
                    ele_open = false;
                    if (ts > -1) {
                        mark_tag = ts;
                    }
                }
                else {
                    mark_tag = p;
                }
                attr = null;
                tag[0] = null;
                text = true;
            }
        }

        private void EBLK(Object N, int T) {
            CAT(tag, p - T + 1);
            ELE(N);
        }

